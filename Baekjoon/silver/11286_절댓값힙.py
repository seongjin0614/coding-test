import sys
import heapq

input = sys.stdin.readline
n = int(input())

heap = []

for _ in range(n):
    x = int(input())
    if x != 0:
        # (절댓값, 원래값) 튜플로 저장 → 정렬 기준 맞춤
        heapq.heappush(heap, (abs(x), x))
    else:
        if heap:
            # 절댓값이 가장 작고, 같다면 더 작은 수 반환
            print(heapq.heappop(heap)[1])
        else:
            print(0)
