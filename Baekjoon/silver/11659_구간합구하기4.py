# : 누적 합
    
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <수도코드>
# 수의개수 n과 연산 횟수 m을 입력 받음
# n개의 수를 입력 받아 리스트로 저장
# m의 수만큼 반복하여
# i,j를 입력 받아
# 리스트[i-1]부터 리스트[j]까지의 합을 출력
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <문제풀이>
# n,m = map(int, input().split())
# data = list(map(int, input().split()))
# for _ in range(m):
