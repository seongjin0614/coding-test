#   answer = sum(data[i-1:j])
#   print(answer)
# 시간 초과
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <정답 및 다른풀이>

import sys

# 1️⃣ 입력 받기
n, m = map(int, sys.stdin.readline().split())
data = list(map(int, sys.stdin.readline().split()))

# 2️⃣ 누적 합 배열 생성 (0번 인덱스 사용 → 크기 n+1)
prefix_sum = [0] * (n + 1)

for i in range(1, n + 1):
    prefix_sum[i] = prefix_sum[i - 1] + data[i - 1]  # 누적 합 계산

# 3️⃣ 쿼리 처리 (O(1))
for _ in range(m):
    i, j = map(int, sys.stdin.readline().split())
    print(prefix_sum[j] - prefix_sum[i - 1])  # 구간 합 계산
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <문제를 통한 학습 내용>
# ✅ 슬라이싱(data[start:end])의 end 값은 포함되지 않음!
# ✅ j-1이 아니라 j까지 포함되도록 data[i-1:j]로 해야 함!