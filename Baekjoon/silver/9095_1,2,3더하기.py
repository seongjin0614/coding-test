# <정답 및 다른풀이>
def count_ways(n):
    # DP 배열 생성 (최대 크기는 n+1)
    dp = [0] * (n + 1)
    # 초기값 설정
    dp[1] = 1
    if n >= 2:
        dp[2] = 2
    if n >= 3:
        dp[3] = 4

    # DP 점화식 계산
    for i in range(4, n + 1):
        dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3]

    return dp[n]

# 테스트 케이스 입력
T = int(input())
for _ in range(T):
    n = int(input())
    print(count_ways(n))
