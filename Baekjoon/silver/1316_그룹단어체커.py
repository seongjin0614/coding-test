# <문제풀이>
n = int(input())
count = 0

for _ in range(n):
  word = input()
  seen = set()
  prev = ""
  is_group = True
  
  for char in word:
    if char != prev:
      if char in seen:
        is_group = False
        break
      seen.add(char)
    prev = char
    
  if is_group:
    count += 1

print(count)



