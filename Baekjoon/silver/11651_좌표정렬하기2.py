# <수도코드>
'''
n을 입력 받음
n개의 개수만큼 좌표를 입력 받아서
이차원배열로 저장
[1]순으로 정렬 하고 [0] 순으로 정렬
순차적 출력
'''
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <문제풀이>
n = int(input())

coordinate_list = []

for _ in range(n):
    coordinate = list(map(int, input().split()))
    coordinate_list.append(coordinate)
    
coordinate_list.sort(key=lambda x: (x[1], x[0]))

for i in coordinate_list:
    print(*i)

