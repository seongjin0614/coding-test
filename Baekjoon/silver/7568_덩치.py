'''
n을 입력 받음.
n명만큼 몸무게, 키를 입력받음
n명의 등수를 기록할 리스트 생성

[0]보다 [1~n] 까지의 사람들 중에서 키, 몸무게가
더 큰 사람이 있다면 count += 1

등수 기록 리스트에서 []의 count 저장

등수 기록 리스트 출력
'''
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <문제풀이>
n = int(input())

result = []
result_count = 0
information_list = []

for i in range(n):
    weight, height = map(int, input().split())
    information = [weight, height]
    information_list.append(information)
    
for i in information_list:
    count = 1
    for j in information_list:
        if j[0] > i[0] and j[1] > i[1]:
            count += 1
    result.append(count)
    
print(*result)
    
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <정답 및 다른풀이>

n = int(input())
people = [tuple(map(int, input().split())) for _ in range(n)]

ranks = [1] * n  # 모든 사람의 초기 등수는 1

for i in range(n):
    for j in range(n):
        if i != j:  # 자신과 비교하지 않음
            if people[i][0] < people[j][0] and people[i][1] < people[j][1]:
                ranks[i] += 1  # 자신보다 키와 몸무게가 모두 크면 등수 증가

print(*ranks)

