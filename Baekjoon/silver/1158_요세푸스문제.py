from collections import deque

def josephus(n, k):
    queue = deque(range(1, n + 1))  # 1부터 N까지 사람을 큐에 저장
    result = []  # 제거된 사람들을 저장할 리스트

    while queue:
        queue.rotate(-(k - 1))  # K-1번 만큼 왼쪽으로 회전 (K번째 사람이 맨 앞에 오도록)
        result.append(queue.popleft())  # K번째 사람 제거

    return f"<{', '.join(map(str, result))}>"

# 입력 처리
n, k = map(int, input().split())
print(josephus(n, k))
