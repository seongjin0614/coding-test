# <수도코드>
# n, m 을 입력받음
# n개의 m개의 원소를 가진 배열을 하나의 배열로 입력받음
# 반복하여 행렬B를 입력받음 -> 행렬A와 더함
# ------------------------------------------------------------------------------------------------------------------------------------------------   
# <문제풀이>
n,m = map(int,input().split())
a = [list(map(int,input().split()))for _ in range(n)]
b = [list(map(int,input().split()))for _ in range(n)]
for i in range(n):
    for j in range(m):
        a[i][j] += b[i][j]
for i in a:
    print(*i)
